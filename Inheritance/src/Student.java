
public class Student extends Person {
	private String college;
	private int percentage;
	
	public void setCollege(String clg)
	{
		college = clg;
	}
	public void setPercentage(int Pr)
	{
		percentage = Pr;
	}
	
	public int getPercentage()
	{
		return percentage;
	}
	public String getCollege()
	{
		return college;
	}
	
	

}
